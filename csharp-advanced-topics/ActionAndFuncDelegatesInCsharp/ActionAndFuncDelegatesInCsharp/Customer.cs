namespace ActionAndFuncDelegatesInCsharp;


public class Customer
{
    public string Name { get; set; }
    public int Age { get; set; }
    public string ShirtSize { get; set; }

    public Customer(string name, int age, string shirtSize)
    {
        Name = Name;
        Age = Age;
        ShirtSize = shirtSize;
    }
}